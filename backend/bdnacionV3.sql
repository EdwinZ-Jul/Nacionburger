CREATE TABLE Clientes (
    ID_CLIENTE INT PRIMARY KEY IDENTITY(1,1),
    NOMBRES VARCHAR(100) NOT NULL,
    APELLIDOS VARCHAR(100) NOT NULL,
    DNI VARCHAR(8) NULL, -- 🔥 ahora permite NULL
    TELEFONO VARCHAR(20),
    EMAIL VARCHAR(100) NOT NULL UNIQUE
);

-- 🔥 índice único filtrado (correcto)
CREATE UNIQUE NONCLUSTERED INDEX IDX_Clientes_DNI_Filtered 
ON Clientes(DNI)
WHERE DNI IS NOT NULL;


CREATE TABLE Cargos (
    ID_CARGO INT PRIMARY KEY,
    DESCRIPCION VARCHAR(50) NOT NULL
);

CREATE TABLE Horarios (
    ID_HORARIO INT PRIMARY KEY IDENTITY(1,1),
    HORA_INICIO TIME NOT NULL,
    HORA_FIN TIME NOT NULL
);

CREATE TABLE Trabajadores (
    ID_TRABAJADOR INT PRIMARY KEY IDENTITY(1,1),
    NOMBRES VARCHAR(100) NOT NULL,
    APELLIDOS VARCHAR(100) NOT NULL,
    DNI VARCHAR(8) NOT NULL UNIQUE,
    TELEFONO VARCHAR(20),
    EMAIL VARCHAR(100) NOT NULL,
    ID_CARGO INT NOT NULL,
    ID_HORARIO INT NOT NULL,
    FOREIGN KEY (ID_CARGO) REFERENCES Cargos(ID_CARGO),
    FOREIGN KEY (ID_HORARIO) REFERENCES Horarios(ID_HORARIO)
);

-- ========================
-- USUARIOS (CORREGIDO)
-- ========================
CREATE TABLE Usuarios (
    ID_USUARIO INT PRIMARY KEY IDENTITY(1,1),
    USUARIO VARCHAR(100) NOT NULL UNIQUE, -- 🔥 ampliado
    CONTRASENA VARCHAR(255) NULL, -- 🔥 ahora permite NULL
    GOOGLE_ID VARCHAR(255) NULL, -- 🔥 nuevo campo

    ID_CLIENTE INT NULL,
    ID_TRABAJADOR INT NULL,
    TIPO_USUARIO VARCHAR(20) NOT NULL DEFAULT 'cliente',

    FOREIGN KEY (ID_CLIENTE) REFERENCES Clientes(ID_CLIENTE),
    FOREIGN KEY (ID_TRABAJADOR) REFERENCES Trabajadores(ID_TRABAJADOR),

    CHECK (
        (ID_CLIENTE IS NOT NULL AND ID_TRABAJADOR IS NULL)
        OR
        (ID_CLIENTE IS NULL AND ID_TRABAJADOR IS NOT NULL)
    )
);

-- 🔥 índice único filtrado para GOOGLE_ID
CREATE UNIQUE NONCLUSTERED INDEX IDX_Usuarios_GOOGLE_ID
ON Usuarios(GOOGLE_ID)
WHERE GOOGLE_ID IS NOT NULL;

